package com.wishare.demo.entity;

public class PersonEntity {

    private Integer perId;
    private String perName;
    private String perPwd;
    private String perSex;
    private Integer perAge;

    public Integer getPerId() {
        return perId;
    }

    public void setPerId(Integer perId) {
        this.perId = perId;
    }

    public String getPerName() {
        return perName;
    }

    public void setPerName(String perName) {
        this.perName = perName;
    }

    public String getPerPwd() {
        return perPwd;
    }

    public void setPerPwd(String perPwd) {
        this.perPwd = perPwd;
    }

    public String getPerSex() {
        return perSex;
    }

    public void setPerSex(String perSex) {
        this.perSex = perSex;
    }

    public Integer getPerAge() {
        return perAge;
    }

    public void setPerAge(Integer perAge) {
        this.perAge = perAge;
    }
}
